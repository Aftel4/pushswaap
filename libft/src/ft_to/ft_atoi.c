#include "libft.h"

int	ft_atoi(const char *str)
{
	int			signe;
	long int	cpy;
	int			i;

	signe = 1;
	cpy = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		cpy *= 10;
		cpy += (int)str[i] - '0';
		i++;
	}
	return (signe * cpy);
}

#include "libft.h"

int	ft_isalnum(int c)
{
	int	est_alnum;

	est_alnum = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9'))
		est_alnum = 1;
	return (est_alnum);
}

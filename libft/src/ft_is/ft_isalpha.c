#include "libft.h"

int	ft_isalpha(int c)
{
	int	est_alpha;

	est_alpha = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		est_alpha = 1;
	return (est_alpha);
}
